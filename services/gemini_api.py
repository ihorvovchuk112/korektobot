async def process_text_with_gemini(text: str, mode: str) -> str:
    config_data = MODES_CONFIG.get(mode, MODES_CONFIG["mode_full"])
    
    max_retries = 3
    base_delay = 2

    for attempt in range(max_retries):
        try:
            generation_config = types.GenerateContentConfig(
                system_instruction=config_data["instruction"],
                temperature=config_data["temperature"],
                top_p=config_data["top_p"]
            )
            
            logger.info(f"Sending request to Gemini API. Mode: {mode}, Temp: {config_data['temperature']}")
            
            response = await client.aio.models.generate_content(
                model=MODEL_NAME,
                contents=text,
                config=generation_config
            )
            
            logger.info("Successfully received response from Gemini API.")
            return response.text

        except (google.api_core.exceptions.ServiceUnavailable, 
                google.api_core.exceptions.InternalServerError) as e:
            if attempt < max_retries - 1:
                wait_time = base_delay * (attempt + 1)
                logger.warning(f"Gemini server is unavaible ({e.code}). Trying again in{wait_time}s...")
                await asyncio.sleep(wait_time)
                continue
            
            logger.error(f"All tries have been exhausted ({max_retries}). Last error: {e}")
            raise Exception("Gemini Error.")

        except Exception as e:
            logger.error(f"Critical Gemini API error: {e}")
            raise Exception("Gemini Error.")